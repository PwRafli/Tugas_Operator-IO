import math

akar = math.sqrt(26)
pangkat = math.pow(2,3)
bulat_bawah = math.floor(2.4)
bulat_atas = math.ceil(2.4)
print()
print(f"akar 26     = {akar}")
print(f"2 pangkat 3 = {pangkat}")
print(f"pembulatan ke bawah dari 2.4 = {bulat_bawah}")
print(f"pembulatan ke atas dari 2.4  = {bulat_atas}")
print()